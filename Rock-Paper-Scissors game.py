if x == "Rock":
    print("Paper!")
    print("Hahaha, I win!")

elif x == "Paper":
    print("Scissor!")
    print("Hahaha, I win!")

elif x == "Scissor":
    print("Rock!")
    print("Hahaha, I win!")

else:
    print("Bruh!?")