# Basic Arithmetics:-
# Letting (a = constant)

# Addition(+):
# (= + a) or (+= a)

friend = 1
friends = friend + 1

print(friends)

# Subtraction(-):
# (= - a) or (-= a)

Burger = 5
Burgers = Burger - 3

print(Burgers)

# Multiplication(*)
# (= * a) or (*= a)

Chocolate = 9
Chocolates = Chocolate * 5

print(Chocolates)

# Division(/):
# (= / a) or (/= a)

Packet_of_chip = 8
Packet_of_chips = Packet_of_chip / 4

print(Packet_of_chips)

# Exponents(**):
# (= ** a) or (**= a)

Gum = 7
Gums = Gum ** 2

print(Gums)

# Remainders{Modulus}(%):
# (= % a) or (%= a)

Sibling = 9
Siblings = Sibling % 2

print(Siblings)

